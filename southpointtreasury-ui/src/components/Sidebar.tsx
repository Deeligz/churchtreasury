interface NavItem {
  id: string;
  icon: React.ReactNode;
  href: string;
}

const navItems: NavItem[] = [
  { id: "dashboard", icon: <HomeIcon />, href: "/dashboard" },
  { id: "offerings", icon: <OfferingIcon />, href: "/offerings" },
  { id: "accounts", icon: <BudgetIcon />, href: "/accounts" },
  { id: "members", icon: <MembersIcon />, href: "/members" },
];

interface SidebarProps {
  isOpen?: boolean;
  onClose?: () => void;
}

export default function Sidebar({ isOpen = false, onClose }: SidebarProps) {
  const pathname = usePathname();

  const getActiveNav = () => {
    if (pathname === "/dashboard") return "dashboard";
    if (pathname === "/offerings") return "offerings";
    if (pathname === "/accounts") return "accounts";
    if (pathname === "/members") return "members";
    return "dashboard";
  };

  const activeNav = getActiveNav();

  const handleNavClick = () => {
    if (onClose) {
      onClose();
    }
  };

  return (
    <aside className={`sidebar ${isOpen ? "mobile-open" : ""}`}>
      <Link href="/dashboard" onClick={handleNavClick}>
        <div className="sidebar-logo">
          <img src="/Seventh Day Adventist Church.svg" alt="SDA Logo" className="sidebar-sda-logo" />
        </div>
      </Link>

      <nav className="sidebar-nav">
        {navItems.map((item) => (
          <Link key={item.id} href={item.href} onClick={handleNavClick}>
            <div
              className={`sidebar-nav-item ${activeNav === item.id ? "active" : ""}`}
            >
              {item.icon}
            </div>
          </Link>
        ))}
      </nav>

      <div className="sidebar-bottom">
        <div className="sidebar-nav-item">
          <SettingsIcon />
        </div>
        <Link href="/signin">
          <div className="sidebar-nav-item">
            <LogoutIcon />
          </div>
        </Link>
      </div>
    </aside>
  );
}

